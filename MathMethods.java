    public void popupExit() {        
        // open a JOptionPane and exit if count > 1
        if (count == 0) {
            JOptionPane.showMessageDialog(null, "Leaving without saying hi?");
            count++;
            return;
        }
        else {
            JOptionPane.showMessageDialog(null, "Bye!");
            System.exit(0);
        }
    }
    
