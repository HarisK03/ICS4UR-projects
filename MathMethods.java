    private void btnAbsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbsActionPerformed
        // uses .abs() method in Math class and displays absolute value of input
        try {
            double inputAbsVal = Double.parseDouble(inputAbs.getText());
            outputAbs.setText(Double.toString(Math.round((Math.abs(inputAbsVal))*100)/100.0));
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ensure all inputs are numeric!");
        }
    }//GEN-LAST:event_btnAbsActionPerformed

    private void btnSqrtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSqrtActionPerformed
        // uses .sqrt() method in Math class and displays square root of input
        try {
            double inputSqrtVal = Double.parseDouble(inputSqrt.getText());
            // no real roots for negative sqrt
            if (inputSqrtVal < 0) {
                JOptionPane.showMessageDialog(null, "Can't square a negative!");
                return;
            }
            outputSqrt.setText(Double.toString(Math.round((Math.sqrt(inputSqrtVal))*100)/100.0));
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ensure all inputs are numeric!");
        }
    }//GEN-LAST:event_btnSqrtActionPerformed

