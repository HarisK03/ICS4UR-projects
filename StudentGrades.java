    // get student average
    private void btnSAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSAActionPerformed
        try {
            String sName = inputFirstN.getText() + " " + inputLastN.getText();
            if (sName.isEmpty()) {
                JOptionPane.showMessageDialog(null, "You have an empty value!");
            }
            else {
                for (int i = 0; i < sData.length; i++) {
                    // skip null
                    if (sData[i][0] == null) {}
                    else if (sData[i][0].equals(sName)) {
                        double sAvrg = ((Double.parseDouble(sData[i][1]) + Double.parseDouble(sData[i][2]) + Double.parseDouble(sData[i][3]) + Double.parseDouble(sData[i][4]))/4);
                        display.setText("The average of " + sName + " is: " + Double.toString(sAvrg) + "%." + "\n");
                        display.append(Arrays.deepToString(sData[i]) + "\n");
                        return;
                    } 
                    else {
                        assert true; 
                    }
                }
                JOptionPane.showMessageDialog(null, "Student does not exist!");
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Invalid input!");
        }
    }//GEN-LAST:event_btnSAActionPerformed

