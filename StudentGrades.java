    // collect all marks from all students and divide by amount of marks to get course average
    private void btnCAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCAActionPerformed
        
            double sum = 0;
            double count = 0;
            for (int i = 0; i < sData.length; i++) {
                for (int j = 1; j < sData[i].length; j++) {
                    if (sData[i][j] != null) {
                        sum += Double.parseDouble(sData[i][j]);
                        count++;
                    }
                } 
            }
            display.setText("The course average is " + Double.toString(sum/count) + "%." + "\n");
            for (int k = 0; k < a; k++) {
                display.append(Arrays.deepToString(sData[k]) + "\n");
            }
    }//GEN-LAST:event_btnCAActionPerformed

    // add name and marks into the array
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String sName = inputFirstN.getText() + " " + inputLastN.getText(); 
            sData[a][0] = sName;
            sData[a][1] = inputTest1.getText();
            sData[a][2] = inputTest2.getText();
            sData[a][3] = inputTest3.getText();
            sData[a][4] = inputTest4.getText();
            
            if (inputFirstN.getText().isEmpty() || inputLastN.getText().isEmpty() || sData[a][1].isEmpty() || sData[a][2].isEmpty() || sData[a][3].isEmpty() || sData[a][4].isEmpty()) {
                JOptionPane.showMessageDialog(null, "You have an empty value!");
            }
            else if (Double.parseDouble(sData[a][1]) > 100 || Double.parseDouble(sData[a][1]) < 0 || Double.parseDouble(sData[a][2]) > 100 || Double.parseDouble(sData[a][2]) < 0 || Double.parseDouble(sData[a][3]) > 100 || Double.parseDouble(sData[a][3]) < 0 || Double.parseDouble(sData[a][4]) > 100 || Double.parseDouble(sData[a][4]) < 0 || (inputFirstN.getText().isEmpty() && inputLastN.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Hmm those marks look fishy.");
            }
            else {
                a++;
                display.setText("");
                for (int i = 0; i < a; i++) {
                    display.append(Arrays.deepToString(sData[i]) + "\n");
                }
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Max students reached or invalid input!");
            String sName = null; 
            sData[a][0] = null;
            sData[a][1] = null;
            sData[a][2] = null;
            sData[a][3] = null;
            sData[a][4] = null;
        }
    }                                      
    
